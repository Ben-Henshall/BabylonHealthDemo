import UIKit

/// Extension to store color styles using static constants.
extension UIColor {
  public static let titleBlue = UIColor(red: 0.23, green: 0.40, blue: 0.59, alpha: 1) // #3C677F
  public static let powderBlue = UIColor(red: 0.47, green: 0.80, blue: 1, alpha: 0.1) // #79CDFF
  public static let pastelBlue = UIColor(red: 0.17, green: 0.67, blue: 1, alpha: 1) // #2CAAFF
}
